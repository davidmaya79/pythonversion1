num1 = int(input("Ingrese un valor : ")) # Solicita al usuario que ingrese un valor y lo almacena en la variable num1 como un número entero.
x = 1 # se inicializa la variable x en 1.
while x <= num1: # mientras x sea menor o igual a num1, el ciclo while se ejecutará.
    print(x) # imprime el valor actual de x en la consola.
    x = x + 1 # incrementa el valor de x en 1 en cada iteración del ciclo.
